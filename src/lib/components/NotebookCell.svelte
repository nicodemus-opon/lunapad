	function sqlTypeToDataKind(sqlType: string): 'numeric' | 'date' | 'boolean' | 'text' {
		const t = sqlType.toUpperCase();
		if (/INT|DECIMAL|NUMERIC|DOUBLE|FLOAT|REAL/.test(t)) return 'numeric';
