	interface Props {
		cell: Cell;
		index: number;
		isFirst: boolean;
		isLast: boolean;
		dark?: boolean;
		prevCellSources?: GUISourceSchema[];
		notebookId?: string;
		autoRun?: boolean;
		/** Presentation mode: render as output-only with cell chrome hidden. */
		reportView?: boolean;
		/** Cell is currently being generated by AI — shows ghost styling. */
		isGhost?: boolean;
		/** Called when the user clicks "Share with AI" in the cell header. */
		onShareWithAI?: () => void;
