						columnDescriptions={isDbtProject ? columnDescriptions : undefined}
						onColumnDescriptionChange={isDbtProject ? handleColumnDescriptionChange : undefined}
					>
						{#snippet toolbarActions()}
							{#if onOpenResultTab}
								<div
									class="flex items-center gap-1 transition-opacity duration-(--motion-fast) ease-(--motion-ease-out) {showResultControls
										? 'opacity-100'
										: 'pointer-events-none opacity-0'}"
									aria-hidden={!showResultControls}
								>
									<Tooltip.Root>
										<Tooltip.Trigger>
											<Button
												variant="ghost"
												size="sm"
												class="h-6 w-6 p-0"
												onclick={() =>
													onOpenResultTab!(
														cell.id,
														notebookId,
														cell.outputName || `result${index + 1}`,
														cell.resultViewMode
													)}><ExternalLink class="h-3 w-3" /></Button
											>
										</Tooltip.Trigger>
										<Tooltip.Content><p class="text-xs">Open in full tab</p></Tooltip.Content>
									</Tooltip.Root>
								</div>
							{/if}
						{/snippet}
					</InlineResultView>
					</div>
				{/if}
			</div>
		{/if}

		{#if hasStatusLine}
			<CellStatusLine
				{cell}
				{showResult}
				{running}
				{intelligenceSummary}
				connectionName={cellConnectionName}
				{isDbtProject}
				onOpenMaterialize={() => (materializeDialogOpen = true)}
				onShowSql={() => (sqlExpanded = true)}
				onRunTests={() => void testCell(cell.id)}
				onOverlayChange={handleOverlayChange}
				onOpenFull={onOpenResultTab && cell.result && cell.result.rows.length > 50
					? () =>
							onOpenResultTab!(
								cell.id,
								notebookId,
								cell.outputName || `result${index + 1}`,
								'table'
							)
					: undefined}
			/>
		{/if}
	{/snippet}

	{#snippet cellMain()}
		{#if !collapsed}
			{#if worksheet}
				<div class="flex min-h-0 flex-1 flex-col">
					<div
						style="height: {editorHeight}px"
						class="flex min-h-40 shrink-0 flex-col overflow-hidden"
					>
						{#if !codeHidden}
							<div onfocusin={onEditorFocus} class="flex min-h-0 flex-1 flex-col overflow-hidden">
								{@render editorInner()}
							</div>
						{/if}
					</div>
					<div
						class="group flex h-2 w-full shrink-0 cursor-ns-resize touch-none items-center justify-center select-none"
						role="separator"
						aria-label="Resize editor"
						onpointerdown={onSplitResizeStart}
						onpointermove={onSplitResizeMove}
						onpointerup={onSplitResizeEnd}
						onpointercancel={onSplitResizeEnd}
